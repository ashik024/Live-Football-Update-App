package com.example.footballlive;

class ListTableFrance {

    public String positionF;
    public String teamF;
    public String playedF;
    public String wonF;
    public String drawF;
    public String lostF;
    public String gdF;
    public String ptsF;
    public String seasonF;

    public ListTableFrance(String positionF, String teamF, String playedF, String wonF, String drawF, String lostF, String gdF, String ptsF, String seasonF) {
        this.positionF = positionF;
        this.teamF = teamF;
        this.playedF = playedF;
        this.wonF = wonF;
        this.drawF = drawF;
        this.lostF = lostF;
        this.gdF = gdF;
        this.ptsF = ptsF;
        this.seasonF = seasonF;
    }

    public String getPositionF() {
        return positionF;
    }

    public String getTeamF() {
        return teamF;
    }

    public String getPlayedF() {
        return playedF;
    }

    public String getWonF() {
        return wonF;
    }

    public String getDrawF() {
        return drawF;
    }

    public String getLostF() {
        return lostF;
    }

    public String getGdF() {
        return gdF;
    }

    public String getPtsF() {
        return ptsF;
    }

    public String getSeasonF() {
        return seasonF;
    }
}
