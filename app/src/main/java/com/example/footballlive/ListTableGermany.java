package com.example.footballlive;

class ListTableGermany {
    public String positionG;
    public String teamG;
    public String playedG;
    public String wonG;
    public String drawG;
    public String lostG;
    public String gdG;
    public String ptsG;
    public String seasonG;

    public ListTableGermany(String positionG, String teamG, String playedG, String wonG, String drawG, String lostG, String gdG, String ptsG, String seasonG) {
        this.positionG = positionG;
        this.teamG = teamG;
        this.playedG = playedG;
        this.wonG = wonG;
        this.drawG = drawG;
        this.lostG = lostG;
        this.gdG = gdG;
        this.ptsG = ptsG;
        this.seasonG = seasonG;
    }

    public String getPositionG() {
        return positionG;
    }

    public String getTeamG() {
        return teamG;
    }

    public String getPlayedG() {
        return playedG;
    }

    public String getWonG() {
        return wonG;
    }

    public String getDrawG() {
        return drawG;
    }

    public String getLostG() {
        return lostG;
    }

    public String getGdG() {
        return gdG;
    }

    public String getPtsG() {
        return ptsG;
    }

    public String getSeasonG() {
        return seasonG;
    }
}
