package com.example.footballlive;

class ListTableSpain {
    public String positionS;
    public String teamS;
    public String playedS;
    public String wonS;
    public String drawS;
    public String lostS;
    public String gdS;
    public String ptsS;
    public String seasonS;

    public ListTableSpain(String positionS, String teamS, String playedS, String wonS, String drawS, String lostS, String gdS, String ptsS, String seasonS) {
        this.positionS = positionS;
        this.teamS = teamS;
        this.playedS = playedS;
        this.wonS = wonS;
        this.drawS = drawS;
        this.lostS = lostS;
        this.gdS = gdS;
        this.ptsS = ptsS;
        this.seasonS = seasonS;
    }

    public String getPositionS() {
        return positionS;
    }

    public String getTeamS() {
        return teamS;
    }

    public String getPlayedS() {
        return playedS;
    }

    public String getWonS() {
        return wonS;
    }

    public String getDrawS() {
        return drawS;
    }

    public String getLostS() {
        return lostS;
    }

    public String getGdS() {
        return gdS;
    }

    public String getPtsS() {
        return ptsS;
    }

    public String getSeasonS() {
        return seasonS;
    }
}
